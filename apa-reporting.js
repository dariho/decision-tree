function buildApaPack(language) {
  const metricLabels = apaMetricTranslations[language] || {};
  return {
    ...apaLabels[language],
    tests: Object.fromEntries(
      Object.entries(apaTemplates[language]).map(([resultId, template]) => [
        resultId,
        { template, metrics: (apaMetrics[resultId] || []).map((metric) => metricLabels[metric] || metric) }
      ])
    )
  };
}

window.apaReportingPacks = Object.fromEntries(
  Object.keys(apaTemplates).map((language) => [language, buildApaPack(language)])
);
